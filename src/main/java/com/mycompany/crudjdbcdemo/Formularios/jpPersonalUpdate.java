    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        PersonalDaoimp per=PersonalDaoimp.getInstance();
        try{      
            int i =Integer.parseInt(txtid1.getText());
            Personal c;
            
            
            
            c = new Personal(txtdni.getText(),txtnombre.getText(),txtapellido1.getText(),txtapellido2.getText(),Integer.parseInt(txttipo.getText()));
            c.setCp(txtcp.getText());
            c.setDireccion(txtdireccion.getText());
            c.setEmail(txtemail.getText());
            c.setId(Integer.parseInt(txtid1.getText()));
            c.setPoblacion(txtpoblacion.getText());
            c.setProvincia(txtprovincia.getText());
            c.setTelefono(txttelefono.getText());
            per.update(i,c);
            JOptionPane.showMessageDialog(this, "Personal actualizado correctamente,cierra esta pesta√±a para ver los cambios");
        }catch(Exception e){
            System.out.println("Error:"+e.getMessage());
         } 
    }//GEN-LAST:event_btnupdateActionPerformed


