     private Unidad getCampos(){
        Unidad c=new Unidad();
       
        c.setNombre(txtnombre.getText());
        c.setCodigo(txtcodigo.getText());
        c.setObservaciones(txtobservaciones.getText());
        
        c.setIdaula(aula.get(cbaula.getSelectedIndex()).getId());
        c.setIdcurso(lst.get(comboboxcurso.getSelectedIndex()).getId());
        c.setIdtutor(personal.get(cbtutor.getSelectedIndex()).getId());
        
        return c;        
    }
     public void cargaComboBox(){
        
        CursoDAOimp cursoaca=CursoDAOimp.getInstance();
        try{
             lst=cursoaca.getAll();
            
            for( Curso curso:lst){
                
                comboboxcurso.addItem(curso.getCodigo());
            }
            }catch(Exception e){
            System.out.println("Error:"+e.getMessage());
        }
}
    public void cargaComboBoxTutor(){
        
        PersonalDaoimp per=PersonalDaoimp.getInstance();
        try{
             personal=per.getAll();
            
            for( Personal p:personal){
                
                cbtutor.addItem(p.getNombre());
            }
            }catch(Exception e){
            System.out.println("Error:"+e.getMessage());
        }
}
    public void cargaComboBoxAula(){
        
        AulaDaoimp aul=AulaDaoimp.getInstance();
        try{
             aula=aul.getAll();
            
            for( Aula a:aula){
                
                cbaula.addItem(a.getDescripcion());
            }
            }catch(Exception e){
            System.out.println("Error:"+e.getMessage());
        }
}

