    public void UnidadCargaDetalle(int id){
        UnidadDAOimp unidad=UnidadDAOimp.getInstance();
        try{
        Unidad c= unidad.getById(id);
        this.id=id;
        txtid.setText(""+id);
        
        txtnombre.setText(""+c.getNombre());
        txtcodigo.setText(""+c.getCodigo());
        
        txtobservaciones.setText(""+c.getObservaciones());
        
        int posicion=0;
        int posiciontutor=0;
        int posicionaula=0;
        int idCursoSeleccionado = c.getIdcurso();
        int idTutor=c.getIdtutor();
        int idAula=c.getIdaula();
        int j;
        for(j=0;j<lst.size();j++){
            Curso cu=lst.get(j);
            if(cu.getId()==idCursoSeleccionado){
                posicion=j;
                
            }
        }
        comboboxcurso.setSelectedIndex(posicion);
        for(j=0;j<personal.size();j++){
            Personal p=personal.get(j);
            if(p.getId()==idTutor){
                posiciontutor=j;
                
            }
        }
        cbtutor.setSelectedIndex(posiciontutor);
        for(j=0;j<aula.size();j++){
            Aula au=aula.get(j);
            if(au.getId()==idAula){
                posicionaula=j;
                
            }
        }
        cbaula.setSelectedIndex(posicionaula);
        }catch(Exception e){
            System.out.println("Error:"+e.getMessage());
        }
    }
