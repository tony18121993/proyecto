    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        AlumnoDaoimp alum=AlumnoDaoimp.getInstance();
        int i =Integer.parseInt(txtid.getText());
        try{      
            Alumno c;
            c = new Alumno(txtnombre.getText(),txtapellido1.getText());
            c.setApellido2(txtapellido2.getText());
            c.setCp(txtcp.getText());
            c.setDireccion(txtdireccion.getText());
            c.setDni(txtdni.getText());
            c.setEmail(txtemail.getText());
            c.setFnacimiento(LocalDate.parse(txtfechanacimiento.getText()));
            c.setPoblacion(txtpoblacion.getText());
            c.setProvincia(txtprovincia.getText());
            c.setTelefono(txttelefono.getText());
            c.setId(Integer.parseInt(txtid.getText()));

            alum.update(i,c);
            JOptionPane.showMessageDialog(this, "Unidad actualizado correctamente,cierra esta pesta√±a para ver los cambios");
        }catch(Exception e){
            System.out.println("Error:"+e.getMessage());
         } 
    }//GEN-LAST:event_btnUpdateActionPerformed


